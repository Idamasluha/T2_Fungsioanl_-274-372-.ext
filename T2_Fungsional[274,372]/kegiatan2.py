# Membuat tupel
my_tuple = (10, 20, 30, 40, 10, 50)

# Mengakses elemen-elemen dalam tupel
print("Elemen pertama:", my_tuple[0])
print("Elemen kedua:", my_tuple[1])

# Menghitung berapa kali elemen tertentu muncul dalam tupel
count_10 = my_tuple.count(10)
print("Jumlah elemen 10:", count_10)

# Menemukan indeks elemen dalam tupel
index_30 = my_tuple.index(30)
print("Indeks elemen 30:", index_30)